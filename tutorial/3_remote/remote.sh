# Git remote add - Register the remote repository in your local site with the URL
git remote add <REMOTE_REPO> <REPO_URL>
# Git remote - Show all registered remote repository in local site
git remote
# Git remote get-url - Show the URL of the specific remote repository registered in local site
git remote get-url <REMOTE_REPO>
# Git remote rename - Rename the remote repository registered in local site
git remote rename <OLD_NAME> <NEW_NAME>
# Git remote set-url - Change the URL of the remote repository registered in local site
git remote set-url <REMOTE_REPO> <REPO_URL>